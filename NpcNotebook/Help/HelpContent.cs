namespace NpcNotebook.Help;

public sealed record HelpSection(string Heading, string Body);

public static class HelpContent
{
    public const string Title = "Как пользоваться блокнотом NPC";

    public static IReadOnlyList<HelpSection> Sections { get; } =
    [
        new(
            "С чего начать",
            """
            Блокнот помогает мастеру хранить персонажей (NPC), группировать их и быстро находить нужные реплики за столом.

            1. «Открыть» — загрузить сохранённый блокнот (.npcbook) со всеми персонажами, группами и портретами.
            2. «Сохранить» — записать всё в файл .npcbook (откроется диалог выбора пути).
            3. «+ Группа» — создать группу: город, гильдия, фракция или что угодно.
            4. «+ Персонаж» — новый NPC; кликните по нему, чтобы открыть карточку справа.
            5. Перетащите персонажа на заголовок группы — он попадёт в эту группу.

            Один персонаж может быть в нескольких группах сразу. Персонажи без группы показываются в разделе «Без группы».
            """),

        new(
            "Группы и список персонажей",
            """
            • Слева — свёрнутые блоки-группы с карточками: портрет и имя
            • Клик по персонажу — карточка справа
            • Перетаскивание персонажа на название группы — включить в группу
            • Правый клик по персонажу — исключить из группы или удалить
            • Правый клик по заголовку группы — переименовать или удалить группу
            • Поле поиска вверху — фильтр по имени, расе и роду деятельности

            Удаление группы не удаляет персонажей — они просто перестают принадлежать этой группе.
            """),

        new(
            "Карточка персонажа",
            """
            ▸ Портрет — нажмите на изображение или «Загрузить портрет», чтобы выбрать картинку (PNG, JPG, WEBP, BMP).

            ▸ Имя, раса, возраст, род деятельности, характер — обычные текстовые поля. Изменения сохраняются в блокнот при сохранении файла.

            ▸ Отношения — связь с другими персонажами из блокнота. Нажмите «+ Добавить», выберите персonaжа и опишите отношение: «друг детства», «должник», «тайный покровитель» и т.д.
            """),

        new(
            "Диалоги и реплики",
            """
            Внизу карточки — вкладки с текстом для игры:

            • «+ Вкладка» — новый диалог или набор реплик
            • Двойной клик по названию вкладки — переименовать («Приветствие», «Торг», «Если спросят про артефакт»)
            • Правый клик по вкладке — переименовать или удалить
            • Большое текстовое поле — сам текст реплик, который вы зачитываете или показываете игрокам

            Называйте вкладки так, как вам удобно искать их во время сессии.
            """),

        new(
            "Сохранение",
            """
            • «Сохранить» — записать блокнот; каждый раз откроется диалог, куда сохранить файл .npcbook
            • Звёздочка (*) в заголовке окна — есть несохранённые изменения
            • Файл .npcbook — это архив: данные персонажей и портреты в одном месте

            Регулярно сохраняйте блокнот — так вы не потеряете подготовленных NPC.
            """),

        new(
            "Если что-то не так",
            """
            • Не вижу персонажа — проверьте поиск вверху или раздел «Без группы»
            • Портрет не загрузился — используйте PNG или JPG; файл не должен быть повреждён
            • Нет пункта в «Отношения» — нужен хотя бы ещё один персонаж в блокноте
            • Потеряли изменения — нажимайте «Сохранить» чаще; файл .npcbook хранит всё сразу
            • Персonaж в двух группах — так и задумано: один NPC может относиться к городу и к гильдии одновременно
            • Удалили группу — персонажи остались, они просто больше не в этой группе
            """)
    ];
}
